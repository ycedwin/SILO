		<h1>About SILO</h1>
		<p>SILO (Scientific Information for Land Owners) is a database of historical climate records for Australia. It provides daily datasets for a range of <a href="data_available.html">climate variables</a> and in <a href="data_format.html">formats</a> suitable for a variety of applications.</p>
		
		<p>SILO data are an enabling technology which allows clients to focus on their research, without the burden of data preparation. Our products support a wide range of research projects and decision support systems because:</p>
		
		<ul>
  <li>we provide national coverage, with no missing data;</li>

  <li>the datasets are ready to use, in a variety of formats; and</li>

  <li>collaboration is fostered by the use of common datasets.</li>

</ul>

<p></p>

<p>SILO provides data to the Queensland Government, other state and federal agencies, CSIRO, Universities, consultants and landholders.</p>

<p>Current initiatives by the SILO team are to:</p>

<ul>
  <li>maintain and improve the quality of systems delivering historical data;</li>

  <li>incorporate climate change scenarios into the SILO dataset to allow the delivery of daily projections for 2030 and 2050 at any location in Australia;</li>

  <li>develop new ways to deliver&nbsp;<a href="/climateprojections/index.html">climate change scenarios</a> to help analyse climate change impacts and development of adaptation policy by SILO’s clients; and</li>

  <li>raise awareness of SILO’s current and proposed services.</li>

</ul>

<p></p>

<p><strong>SILO data</strong></p>

<p>SILO datasets are constructed from observational records provided by the&nbsp;<a class="linkexternal" href="http://www.bom.gov.au/">Bureau of Meteorology</a>. SILO processes the raw data, which may contain missing values, to derive datasets which both spatially and temporally complete. Our datasets are available through a&nbsp;<a href="/silo/silopricelist.html#fees">paid licencing agreement</a>, although some data are available free of charge. For further information, please consult our <a href="silopricelist.html">price list</a>.</p>

<p>For a brief comparison of the products available from SILO and the Bureau, please see our&nbsp;<a href="/silo/pdf/comparison_of_silo_and_bom_data.pdf">product comparison</a> (PDF, 359K, last updated 02:43PM, 27 July 2015)*. For a detailed analysis of the interpolated surfaces provided by SILO and the Bureau, please see <a href="publications.html#Reviews">Beesley et.al. (2009)</a>.</p>

<p> </p>

<p><strong>Achievements</strong></p>

<p>The SILO historical data system began as a world leading innovation in 1996. Since then SILO has received several state and national awards for excellence.</p>

<p>* Requires <a href="/help/pdf_files.html">Adobe Reader</a></p>
		
		
		
